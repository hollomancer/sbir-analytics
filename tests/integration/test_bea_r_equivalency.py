"""BEA Python ↔ R/StateIO equivalency tests.

Validates that the Python Leontief I-O math produces identical results to the
R/StateIO package.  Uses two approaches:

1. **Synthetic matrices** (always run): 3×3 matrices with known analytical
   solutions.  Validates the math layer independent of any API.

2. **Live BEA API** (requires BEA_API_KEY): Fetches real Use/VA tables from the
   BEA API and compares against reference fixture values generated by the R
   script ``scripts/generate_r_reference_values.R``.  Skipped if API key is
   not set or if reference fixture doesn't contain R-generated values.

To regenerate R reference values (requires R + stateior package):
    Rscript scripts/generate_r_reference_values.R
"""

from __future__ import annotations

import json
from decimal import Decimal
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

from sbir_etl.transformers.bea_io_functions import (
    apply_demand_shocks,
    calculate_employment_from_production,
    calculate_leontief_inverse,
    calculate_technical_coefficients,
)

FIXTURES_DIR = Path(__file__).parent.parent / "fixtures"
REFERENCE_FILE = FIXTURES_DIR / "r_reference_values.json"


@pytest.fixture
def reference_data():
    """Load reference values fixture."""
    with open(REFERENCE_FILE) as f:
        return json.load(f)


@pytest.fixture
def synthetic(reference_data):
    """Extract the synthetic 3×3 test case."""
    return reference_data["synthetic_3x3"]


# ---------------------------------------------------------------------------
# Synthetic matrix tests (always run, no API needed)
# ---------------------------------------------------------------------------


class TestSyntheticMatrixEquivalency:
    """Validate Leontief math against known analytical solutions."""

    def _make_use_table(self, synthetic):
        sectors = synthetic["use_table"]["sectors"]
        values = synthetic["use_table"]["values"]
        return pd.DataFrame(values, index=sectors, columns=sectors, dtype=float)

    def _make_output(self, synthetic):
        sectors = synthetic["use_table"]["sectors"]
        return pd.Series(synthetic["industry_output"], index=sectors, dtype=float)

    def test_technical_coefficients(self, synthetic):
        """A[i,j] = use[i,j] / output[j] matches expected values."""
        use_table = self._make_use_table(synthetic)
        output = self._make_output(synthetic)

        tech_coeff = calculate_technical_coefficients(use_table, output)

        expected = np.array(synthetic["tech_coefficients"]["values"])
        np.testing.assert_allclose(
            tech_coeff.values, expected, atol=1e-4,
            err_msg="Technical coefficients diverge from reference",
        )

    def test_leontief_inverse(self, synthetic):
        """L = (I - A)^{-1} matches expected values within tolerance."""
        use_table = self._make_use_table(synthetic)
        output = self._make_output(synthetic)

        tech_coeff = calculate_technical_coefficients(use_table, output)
        leontief_inv = calculate_leontief_inverse(tech_coeff)

        expected = np.array(synthetic["leontief_inverse"]["values"])
        tol = synthetic["leontief_inverse"]["tolerance"]
        np.testing.assert_allclose(
            leontief_inv.values, expected, atol=tol,
            err_msg="Leontief inverse diverges from reference",
        )

    def test_demand_shock_production(self, synthetic):
        """Production = L × demand matches expected per-sector output."""
        use_table = self._make_use_table(synthetic)
        output = self._make_output(synthetic)
        sectors = synthetic["use_table"]["sectors"]

        tech_coeff = calculate_technical_coefficients(use_table, output)
        leontief_inv = calculate_leontief_inverse(tech_coeff)

        shock_cfg = synthetic["demand_shock"]
        shocks_df = pd.DataFrame({
            "bea_sector": [shock_cfg["shock_sector"]],
            "shock_amount": [shock_cfg["shock_amount"]],
        })

        production = apply_demand_shocks(leontief_inv, shocks_df)
        tol = shock_cfg["tolerance"]

        for sector in sectors:
            expected_val = shock_cfg["expected_production"][sector]
            assert abs(float(production[sector]) - expected_val) < tol, (
                f"Sector {sector}: got {production[sector]:.4f}, "
                f"expected {expected_val:.4f} (tol={tol})"
            )

        assert abs(float(production.sum()) - shock_cfg["total_output"]) < tol

    def test_leontief_inverse_identity_property(self, synthetic):
        """L × (I - A) ≈ I (mathematical identity check)."""
        use_table = self._make_use_table(synthetic)
        output = self._make_output(synthetic)

        tech_coeff = calculate_technical_coefficients(use_table, output)
        leontief_inv = calculate_leontief_inverse(tech_coeff)

        identity = np.eye(len(output))
        i_minus_a = identity - tech_coeff.values
        result = leontief_inv.values @ i_minus_a

        np.testing.assert_allclose(
            result, identity, atol=1e-10,
            err_msg="L × (I - A) should equal identity matrix",
        )

    def test_unit_conversion_dollars_to_bea(self, reference_data):
        """Verify dollar ↔ BEA-millions conversion in the adapter pipeline."""
        conv = reference_data["unit_conversion"]

        # $1M in dollars = 1.0 in BEA units
        shock_dollars = conv["shock_dollars"]
        shock_bea = shock_dollars / 1_000_000.0
        assert shock_bea == conv["shock_bea_units"]

        # After Leontief multiplication in BEA units, convert back to dollars
        # Using the synthetic Leontief inverse sector A self-multiplier (~1.2706)
        synthetic = reference_data["synthetic_3x3"]
        use_table = pd.DataFrame(
            synthetic["use_table"]["values"],
            index=synthetic["use_table"]["sectors"],
            columns=synthetic["use_table"]["sectors"],
            dtype=float,
        )
        output = pd.Series(
            synthetic["industry_output"],
            index=synthetic["use_table"]["sectors"],
            dtype=float,
        )
        tech_coeff = calculate_technical_coefficients(use_table, output)
        leontief_inv = calculate_leontief_inverse(tech_coeff)

        shocks_df = pd.DataFrame({
            "bea_sector": ["A"],
            "shock_amount": [shock_bea],
        })
        production = apply_demand_shocks(leontief_inv, shocks_df)

        # Convert back to dollars
        production_dollars = float(production["A"]) * 1_000_000.0
        expected = conv["expected_production_dollars_sector_A"]
        tol = conv["tolerance_dollars"]
        assert abs(production_dollars - expected) < tol, (
            f"Production impact in dollars: {production_dollars:.0f}, "
            f"expected {expected} (tol={tol})"
        )


