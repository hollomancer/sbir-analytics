@pytest.mark.requires_bea_api
class TestLiveBEAEquivalency:
    """Compare live BEA API results against R-generated reference values.

    These tests only run when:
    1. BEA_API_KEY environment variable is set
    2. tests/fixtures/r_reference_values.json contains R-generated data
       (has 'stateior_version' in metadata)

    To generate R reference values:
        Rscript scripts/generate_r_reference_values.R
    """

    @pytest.fixture(autouse=True)
    def _skip_without_r_reference(self, reference_data):
        """Skip if reference fixture doesn't have R-generated values."""
        metadata = reference_data.get("metadata", {})
        if "stateior_version" not in metadata:
            pytest.skip(
                "R-generated reference values not found. "
                "Run: Rscript scripts/generate_r_reference_values.R"
            )

    @pytest.fixture
    def bea_client(self):
        """Create a real BEA API client."""
        from sbir_etl.transformers.bea_api_client import BEAApiClient

        return BEAApiClient()

    def test_use_table_sector_coverage(self, bea_client, reference_data):
        """BEA API Use table has the same sectors as R/StateIO."""
        from sbir_etl.transformers.bea_io_functions import fetch_use_table

        use_table = fetch_use_table(bea_client, year=2020)
        assert not use_table.empty, "BEA Use table fetch returned empty"

        r_sectors = reference_data["tech_coefficients_5x5"]["sectors"]
        for sector in r_sectors:
            assert sector in use_table.columns, (
                f"R reference sector '{sector}' missing from BEA API Use table"
            )

    def test_tech_coefficients_match_r(self, bea_client, reference_data):
        """Technical coefficients from BEA API match R/StateIO within tolerance."""
        from sbir_etl.transformers.bea_io_functions import (
            fetch_industry_output,
            fetch_use_table,
        )

        use_table = fetch_use_table(bea_client, year=2020)
        output = fetch_industry_output(use_table)
        tech_coeff = calculate_technical_coefficients(use_table, output)

        r_tc = reference_data["tech_coefficients_5x5"]
        r_sectors = r_tc["sectors"]

        for i, row_sector in enumerate(r_sectors):
            for j, col_sector in enumerate(r_sectors):
                if row_sector in tech_coeff.index and col_sector in tech_coeff.columns:
                    python_val = tech_coeff.loc[row_sector, col_sector]
                    r_val = r_tc["values"][i][j] if isinstance(r_tc["values"][i], list) else r_tc["values"][row_sector][j]
                    assert abs(float(python_val) - float(r_val)) < 0.01, (
                        f"Tech coeff [{row_sector},{col_sector}]: "
                        f"Python={python_val:.6f}, R={r_val:.6f}"
                    )

    def test_demand_shock_total_output_matches_r(self, bea_client, reference_data):
        """Total production from $1M shock matches R/StateIO within 5%."""
        from sbir_etl.transformers.bea_io_functions import (
            fetch_industry_output,
            fetch_use_table,
        )

        use_table = fetch_use_table(bea_client, year=2020)
        output = fetch_industry_output(use_table)
        tech_coeff = calculate_technical_coefficients(use_table, output)
        leontief_inv = calculate_leontief_inverse(tech_coeff)

        r_shock = reference_data["demand_shock_1m_sector_11"]
        shocks_df = pd.DataFrame({
            "bea_sector": [r_shock["shock_sector"]],
            "shock_amount": [r_shock["shock_amount_millions"]],
        })
        production = apply_demand_shocks(leontief_inv, shocks_df)
        total = float(production.sum())
        r_total = r_shock["total_output"]

        # Allow 5% tolerance for minor BEA table version differences
        pct_diff = abs(total - r_total) / r_total * 100
        assert pct_diff < 5.0, (
            f"Total output: Python={total:.4f}, R={r_total:.4f} "
            f"({pct_diff:.1f}% difference, max 5%)"
        )


