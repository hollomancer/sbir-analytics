@pytest.mark.requires_bea_api
class TestAdapterRoundTrip:
    """End-to-end adapter test with real BEA API."""

    def test_compute_impacts_returns_expected_columns(self):
        """BEAIOAdapter.compute_impacts returns all required columns."""
        from sbir_etl.transformers.bea_io_adapter import BEAIOAdapter

        adapter = BEAIOAdapter(cache_enabled=False)
        if not adapter.is_available():
            pytest.skip("BEA API not available")

        shocks = pd.DataFrame({
            "state": ["CA"],
            "bea_sector": ["11"],
            "fiscal_year": [2020],
            "shock_amount": [Decimal("1000000")],
        })

        result = adapter.compute_impacts(shocks)
        adapter.close()

        required = [
            "state", "bea_sector", "fiscal_year", "shock_amount",
            "wage_impact", "production_impact", "tax_impact",
            "employment_impact", "model_version", "quality_flags",
        ]
        for col in required:
            assert col in result.columns, f"Missing column: {col}"

        assert len(result) == 1
        assert float(result["production_impact"].iloc[0]) > 0

    def test_multi_year_shocks_use_correct_tables(self):
        """Multi-year shocks are split and computed with correct year tables."""
        from sbir_etl.transformers.bea_io_adapter import BEAIOAdapter

        adapter = BEAIOAdapter(cache_enabled=False)
        if not adapter.is_available():
            pytest.skip("BEA API not available")

        shocks = pd.DataFrame({
            "state": ["CA", "CA"],
            "bea_sector": ["11", "11"],
            "fiscal_year": [2019, 2020],
            "shock_amount": [Decimal("1000000"), Decimal("1000000")],
        })

        result = adapter.compute_impacts(shocks)
        adapter.close()

        assert len(result) == 2
        years = result["fiscal_year"].unique().tolist()
        assert 2019 in years
        assert 2020 in years
